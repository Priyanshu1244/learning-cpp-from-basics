#include <iostream>
using namespace std;

int main()
{
    // intilizing i
    int i = 5;

    // performing operation on i using assignment operator

    i += 5; // i = i + 5
    cout << i << endl;

    i -= 5; // i = i - 5
    cout << i << endl;

    i *= 2; // i = i * 2
    cout << i << endl;

    i /= (float)2; // i = i / 2
    cout << i << endl;

    return 0;
}